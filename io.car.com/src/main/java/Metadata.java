
public class Metadata {
	private String color;
	private String notes;

	// Getter Methods

	public String getColor() {
		return color;
	}

	public String getNotes() {
		return notes;
	}

	// Setter Methods

	public void setColor(String color) {
		this.color = color;
	}

	public void setNotes(String notes) {
		this.notes = notes;
	}
}